package module1;

public class PositiveNegativeOrZero {
    public static void main(String[] args) {
        int num = -7;
        if (num > 0)
            System.out.println ("The number is positive");
        else if (num < 0)
            System.out.println ("The number is negative");
        else
            System.out.println ("Zero");
    }
}

